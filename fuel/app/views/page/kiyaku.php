				<li>通信回線やコンピュータなどの障害によるシステムの中断・遅滞・中止・データの消失、データへの不正アクセスにより生じた損害、その他当グループのサービスに関して利用者に生じた損害について、当グループは一切責任を負わないものとします。</li>
				<li>当グループは、当グループのウェブページ・サーバ・ドメインなどから送られるメール・コンテンツに、コンピューター・ウィルスなどの有害なものが含まれていないことを保証いたしません。</li>
